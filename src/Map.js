export default class Map {
  constructor() {
    this.drawer = null;
    this.tileSize = 8;
    this.mapTiles = [];
    this.mapHeight = 0;
    this.mapLength = 0;
  }

  loadLevel(level, colorScheme) {
    let tiles;
    if (level === 1) {
      tiles = level1;
    } else {
      tiles = levels[(level - 1) % 6] || defaultMap;
    }

    this.mapTiles = tiles;
    this.mapHeight = tiles.length;
    this.mapLength = tiles[0].length;

    this.mapWidthPixels = this.tileSize * this.mapLength;
    this.mapHeightPixels = this.tileSize * this.mapHeight;

    this.colorScheme = colorScheme;
  }

  getTile(x, y) {
    const row = this.mapTiles[Math.floor(y / this.tileSize)];
    if (!row) return 1;
    return row[Math.floor(x / this.tileSize)];
  }

  draw(drawer) {
    const [main, highlight, shadow] = this.colorScheme;
    this.drawer = this.drawer || drawer;
    this.mapTiles.forEach((row, y) => {
      row.forEach((tile, x) => {
        if (tile !== 1) return;
        drawer.rect({
          fillColor: main,
          rect: [
            x * this.tileSize + 1,
            y * this.tileSize + 1,
            this.tileSize - 2,
            this.tileSize - 2,
          ],
        });
        drawer.rect({
          fillColor: highlight,
          rect: [
            x * this.tileSize + 1,
            y * this.tileSize,
            this.tileSize - 2,
            1,
          ],
        });
        drawer.rect({
          fillColor: shadow,
          rect: [
            x * this.tileSize + 1,
            y * this.tileSize + this.tileSize - 1,
            this.tileSize - 2,
            1,
          ],
        });
        drawer.rect({
          fillColor: highlight,
          rect: [
            x * this.tileSize,
            y * this.tileSize + 1,
            1,
            this.tileSize - 2,
          ],
        });
        drawer.rect({
          fillColor: shadow,
          rect: [
            x * this.tileSize + this.tileSize - 1,
            y * this.tileSize + 1,
            1,
            this.tileSize - 2,
          ],
        });
      });
    });
  }
}

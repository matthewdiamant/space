const mapToHex = (map) => {
  return map.map((r) => parseInt(r.join(""), 2).toString(16));
};

const hexToMap = (hex) => {
  return hex.map((h) => {
    return parseInt(h, 16).toString(2).split("").map(Number);
  });
};

const defaultMap = hexToMap(level2Hex);
